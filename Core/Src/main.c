/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	mdw_system_initAll(SYSTEM_LOAD_NORMAL);
  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
//  SystemClock_Config();
	mdw_system_initClock(SYSTEM_LOAD_NORMAL);


  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  mdw_led_load();
  mdw_led_open(LED_GREEN_1);
  mdw_log_load();
  mdw_log_open();

  // Creation de la structure qui contient qui va contenir les acceleration 3 axes du capteur
  H3lis331d_Data h3lis331d_data = {0, 0, 0};

  // Init du module qui gère le capteur
  h3lis331dl_init();

  // CS pin should default high
  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_12, GPIO_PIN_SET);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  mdw_led_set(LED_GREEN_1 ,LED_OFF , 0);
	  HAL_Delay(500);
	  mdw_led_set(LED_GREEN_1 ,LED_ON , 0);
	  HAL_Delay(500);

	  // log power up info
	  if (false == isInitDone)
	  {
		  sys_log(SYS_LOG_INFO, "Power up done!");
		  isInitDone = true;
	  }

	  // Lire les données du capteur
	  if (0 != h3lis331d_read_data(&h3lis331d_data))
	  {
		  // log reading error
		  sys_log(SYS_LOG_ERROR, "Error on reading accelerations!");
	  }

	  // Préparer la chaîne à envoyer
	  char buffer[ACCELERATION_BUFFER_SIZE];
	  memset(buffer, 0, sizeof(buffer));
	  snprintf(buffer, sizeof(buffer), "X: %d, Y: %d, Z: %d\r\n", h3lis331d_data.x, h3lis331d_data.y, h3lis331d_data.z);

	  // Afficher les accelerations
	  sys_log(SYS_LOG_DEBUG, buffer);

   }
  /* USER CODE END 3 */

}

